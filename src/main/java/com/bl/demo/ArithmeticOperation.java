package com.bl.demo;

public class ArithmeticOperation {
    public int additionOfNumber(int firstNum, int secondNum) {
        return firstNum + secondNum;
    }

    public int subtractionOfNumber(int firstNum, int secondNum) {
        return firstNum - secondNum;
    }

    public int multiplicationOfNumber(int firstNum, int secondNum) {
        return firstNum * secondNum;
    }

    public int divisionOfNumber(int firstNum, int secondNum) {
        return firstNum / secondNum;
    }

    public int modulusOfNumber(int firstNum, int secondNum) {
        return firstNum % secondNum;
    }
}
